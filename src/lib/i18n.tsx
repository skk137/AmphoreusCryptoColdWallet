export type TFunc = (k: string, ...args: (string | number)[]) => string;

export function makeT(lang: Lang): TFunc {
  return (k, ...args) => {
    let s = dict[lang]?.[k] ?? dict.el[k] ?? k;
    args.forEach((a, i) => (s = s.split(`{${i}}`).join(String(a))));
    return s;
  };
}

export const LangContext = createContext<{ lang: Lang; t: TFunc }>({
  lang: "el",
  t: makeT("el"),
});

export const useT = () => useContext(LangContext);
